"""**# Step 3 - Add Employee Functionality**"""

def add_employee():
    try:
        emp_id = int(input("Enter Employee ID: "))

        if emp_id in employees:
            print("Employee ID already exists. Please enter a unique ID.")
            return

        name = input("Enter Employee Name: ")
        age = int(input("Enter Employee Age: "))
        department = input("Enter Employee Department: ")
        salary = float(input("Enter Employee Salary: "))

        employees[emp_id] = {
            "name": name,
            "age": age,
            "department": department,
            "salary": salary
        }

        print("Employee added successfully!")

    except ValueError:
        print("Invalid input. Please enter correct values.")

# Step 4 - View All Employees
def view_employees():
    if not employees:
        print("No employees available.")
        return

    print("\n===== Employee List =====")
    print("ID\tName\tAge\tDepartment\tSalary")
    print("-----------------------------------------------")

    for emp_id, details in employees.items():
        print(f"{emp_id}\t{details['name']}\t{details['age']}\t{details['department']}\t\t{details['salary']}")

# Step 5 - Search for Employee by ID
def search_employee():
    try:
        emp_id = int(input("Enter Employee ID to search: "))

        if emp_id in employees:
            emp = employees[emp_id]
            print("\nEmployee Found:")
            print(f"ID: {emp_id}")
            print(f"Name: {emp['name']}")
            print(f"Age: {emp['age']}")
            print(f"Department: {emp['department']}")
            print(f"Salary: {emp['salary']}")
        else:
            print("Employee not found.")

    except ValueError:
        print("Invalid input. Please enter a valid ID.")

# Step 6 - Run the Program
main_menu()

