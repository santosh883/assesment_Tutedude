"""**# Step 1 - Data Storage (Pre-filled sample data)**"""

employees = {
    101: {"name": "Satya", "age": 27, "department": "HR", "salary": 50000},
    102: {"name": "Rohan", "age": 30, "department": "IT", "salary": 65000}
}

"""**Menu system**"""

def main_menu():
    print("Employee Management System")
